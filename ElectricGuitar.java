class ElectricGuitar {
    // Stats / Instance Variables
    String brand;
    int numOfPickups;
    boolean rockStarUsesIt;

    // Behaviors
    // Getter & Setter
    String getBrand() { return this.brand; }
    void setBrand(String brand) { this.brand = brand; }

    int getNumOfPickups() { return this.numOfPickups; }
    void setNumOfPickups(int numOfPickups) { this.numOfPickups = numOfPickups; }

    boolean isRockStarUsesIt() { return this.rockStarUsesIt; }
    void setRockStarUsesIt(boolean rockStarUsesIt) { this.rockStarUsesIt = rockStarUsesIt; }

}